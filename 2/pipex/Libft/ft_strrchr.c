#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	const char	*ss;

	ss = s;
	while (*s)
		s++;
	while (*s != (char)c)
	{
		if (s == ss)
			return (0);
		s--;
	}
	return ((char *)s);
}
